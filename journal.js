// Greet a user by name
function greet(name) 
{
  return "Hello, " + name + "!";
}
// Returns the square of a number
function square(x) 
{
  return x * x;
}

// Returns true if the number is even
function isEven(x) 
{
  return x % 2 === 0;
}

// Returns true if the number is odd
function isOdd(x) 
{
  return x % 2 !== 0;
}

// Multiplies two numbers
function multiply(a, b) 
{
  return a * b;
}